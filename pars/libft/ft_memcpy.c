#include "../inc/libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (!src && !dst)
		return (dst);
	while (i < n)
	{
		ft_memset(&dst[i], *((char *)(src + i)), 1);
		i++;
	}
	return (dst);
}
