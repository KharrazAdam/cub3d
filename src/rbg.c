#include "../include/ray.h"

void	rbg(t_colour *col)
{
	col->colour = 0;
	col->colour += (int)(col->tuple.x * 255) << 16 | (int)(col->tuple.y * 255) << 8 | (int)(col->tuple.z * 255);
}